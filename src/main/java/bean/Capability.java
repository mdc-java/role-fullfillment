package bean;

public class Capability {

	private String name;
	private String lead;
	private String talentDemand;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLead() {
		return lead;
	}
	public void setLead(String lead) {
		this.lead = lead;
	}
	public String getTalentDemand() {
		return talentDemand;
	}
	public void setTalentDemand(String talentDemand) {
		this.talentDemand = talentDemand;
	}
}
